package my.com.myriadeas.integral.identityaccess.domain.model;

import my.com.myriadeas.integral.core.domain.model.DomainEvent;

public class UserRemovedFromGroup implements DomainEvent {

	private User user;

	private Group group;

	public UserRemovedFromGroup(User user, Group group) {
		super();
		this.user = user;
		this.group = group;
	}

	public User getUser() {
		return user;
	}

	public void setUser(User user) {
		this.user = user;
	}

	public Group getGroup() {
		return group;
	}

	public void setGroup(Group group) {
		this.group = group;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((group == null) ? 0 : group.hashCode());
		result = prime * result + ((user == null) ? 0 : user.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		UserRemovedFromGroup other = (UserRemovedFromGroup) obj;
		if (group == null) {
			if (other.group != null)
				return false;
		} else if (!group.equals(other.group))
			return false;
		if (user == null) {
			if (other.user != null)
				return false;
		} else if (!user.equals(other.user))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return "UserAddedToGroup [user=" + user + ", group=" + group + "]";
	}

}
